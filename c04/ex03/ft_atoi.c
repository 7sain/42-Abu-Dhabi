int	ft_atoi(char *str)
{
	int	h;
	int	u;
	int	hus;

	h = 0;
	u = 0;
	hus = 0;
	while ((str[h] == '+') || (str[h] == '-')
		|| (str[h] == 32) || (str[h] == '\t'))
	{
		if (str[h] == '-')
		{
					u++;
		}
			h++;
	}
	while ((str[h] >= '0') && (str[h] <= '9'))
	{
			hus = (str[h] - '0') + (hus * 10);
			h++;
	}
	if (u % 2 == 1)
		return (hus * -1);
	else
		return (hus * 1);
}
