char	*ft_strlowcase(char *str)
	{
	char	*h;

	h = str;
	while (*str != '\0')
	{
		if ((*str >= 'A') && (*str <= 'Z'))
		{
			*str = *str + 32;
		}
		str++;
	}
	return (h);
}
